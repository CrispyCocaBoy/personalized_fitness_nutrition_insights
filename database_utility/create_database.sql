-- Switch to the desired database
USE user_device_db;

-- 1. USERS
--    Stores the login credentials of the user
CREATE TABLE users (
  user_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username       VARCHAR(50) NOT NULL UNIQUE,
  password       VARCHAR(255) NOT NULL,
  email          VARCHAR(100) NOT NULL UNIQUE,
  created_at     TIMESTAMPTZ DEFAULT NOW()
);

-- 2. USERS_PROFILE
--    Stores additional demographic information about the user
CREATE TABLE users_profile (
  user_id        INT PRIMARY KEY REFERENCES users(user_id) ON DELETE CASCADE,
  name           VARCHAR(255),
  surname        VARCHAR(255),
  gender         VARCHAR(50),
  birthday       DATE,
  height         NUMERIC(5,2),
  country        VARCHAR(255),
  updated_at     TIMESTAMPTZ DEFAULT NOW()
);

-- 3. WEIGHT
--    Historical weight data for each user
CREATE TABLE weight (
  weight_id      INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id        INT NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
  weight         NUMERIC(5,2) NOT NULL,
  measured_at    TIMESTAMPTZ DEFAULT NOW()
);

-- 4. DEVICE_TYPE
--    Supported device types (watch, band, ring, phone...)
CREATE TABLE device_type (
  device_type_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name           VARCHAR(50) NOT NULL UNIQUE,
  manufacturer   VARCHAR(50),
  model          VARCHAR(50),
  description    TEXT
);

-- 5. DEVICE
--    Physical devices registered by a user
CREATE TABLE device (
  device_id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id           INT NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
  device_name       VARCHAR(100) NOT NULL, -- the user choose this name to recognise the device
  device_type_id    INT NOT NULL REFERENCES device_type(device_type_id) ON DELETE CASCADE,
  serial_number     VARCHAR(100),
  registered_at     TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, serial_number)
);

-- 6. SENSOR_TYPE (TYPE OF HARDWARE)
--    Physical sensors supported by the system
CREATE TABLE sensor_type (
  sensor_type_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name           VARCHAR(50) NOT NULL UNIQUE,
  unit           VARCHAR(20) NOT NULL,
  description    TEXT
);

-- 7. FEATURE (DERIVED METRICS)
--    Features/metrics that can be calculated from sensors
CREATE TABLE feature (
  feature_id     INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name           VARCHAR(50),
  unit           VARCHAR(20),
  sensor_type_id INT NOT NULL REFERENCES sensor_type(sensor_type_id),
  description    TEXT
);

-- 8. PREDEFINED_DEVICE_TYPE_SENSORS
--    Which physical sensors are mounted on each device type
CREATE TABLE predefined_device_type_sensors (
  device_type_id INT NOT NULL REFERENCES device_type(device_type_id) ON DELETE CASCADE,
  sensor_type_id INT NOT NULL REFERENCES sensor_type(sensor_type_id) ON DELETE CASCADE,
  priority       INT NOT NULL DEFAULT 100,
  PRIMARY KEY (device_type_id, sensor_type_id)
);

-- 9. SENSOR_TO_USER (Single sensor associated to the user)
--    Maps physical sensors to user devices
CREATE TABLE sensor_to_user (
  sensor_id      INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id        INT NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
  device_id      INT NOT NULL REFERENCES device(device_id) ON DELETE CASCADE,
  sensor_type_id INT NOT NULL REFERENCES sensor_type(sensor_type_id) ON DELETE CASCADE,
  created_at     TIMESTAMPTZ DEFAULT NOW(),
  custom_name VARCHAR(100),
  UNIQUE(device_id, sensor_type_id)
);
-- 10. USER_FEATURE_PREFERENCES
--     Stores which features each user wants to monitor
CREATE TABLE user_feature_preferences (
  user_id        INT NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
  feature_id     INT NOT NULL REFERENCES feature(feature_id) ON DELETE CASCADE,
  device_id      INT NOT NULL REFERENCES device(device_id) ON DELETE CASCADE,
  updated_at     TIMESTAMPTZ DEFAULT NOW(),
  PRIMARY KEY (user_id, feature_id, device_id)
);

-- 22. SENSOR_STATUS
--     Stato attuale di ciascun sensore (acceso/spento)
CREATE TABLE sensor_status (
    sensor_id INT PRIMARY KEY REFERENCES sensor_to_user(sensor_id) ON DELETE CASCADE,
    active    BOOLEAN NOT NULL DEFAULT TRUE,
    updated_at TIMESTAMPTZ DEFAULT NOW()
);


-- 11. DEFAULT_FOODS
--     Default food database with nutritional information
CREATE TABLE default_foods (
    food_id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    quantity NUMERIC(6,2) DEFAULT 100.00,
    unit VARCHAR(10) NOT NULL, -- 'g' or 'ml'
    calories NUMERIC(6,2),
    carbohydrates NUMERIC(6,2),
    protein NUMERIC(6,2),
    fat NUMERIC(6,2),
    fiber NUMERIC(6,2),
    sugars NUMERIC(6,2),
    saturated_fat NUMERIC(6,2),
    trans_fat NUMERIC(6,2),
    cholesterol NUMERIC(6,2),
    potassium NUMERIC(6,2),
    iron NUMERIC(6,2),
    vitamin_c NUMERIC(6,2),
    vitamin_a NUMERIC(6,2),
    category VARCHAR(100)
);

-- 12. USER_FOODS
--     User-created custom foods with nutritional data
CREATE TABLE user_foods (
    user_food_id SERIAL PRIMARY KEY,
    user_id INT NOT NULL,
    name VARCHAR(255) NOT NULL,
    quantity NUMERIC(6,2) DEFAULT 100.00,
    unit VARCHAR(10) NOT NULL,
    calories NUMERIC(6,2),
    carbohydrates NUMERIC(6,2),
    protein NUMERIC(6,2),
    fat NUMERIC(6,2),
    fiber NUMERIC(6,2),
    sugars NUMERIC(6,2),
    saturated_fat NUMERIC(6,2),
    trans_fat NUMERIC(6,2),
    cholesterol NUMERIC(6,2),
    potassium NUMERIC(6,2),
    iron NUMERIC(6,2),
    vitamin_c NUMERIC(6,2),
    vitamin_a NUMERIC(6,2),
    category VARCHAR(100),
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

-- 13. MEALS
--     User meal records with timestamps and meal types
CREATE TABLE meals (
    meal_id SERIAL PRIMARY KEY,
    user_id INT NOT NULL,
    datetime TIMESTAMP NOT NULL,
    meal_type VARCHAR(100) NOT NULL, -- e.g., Breakfast, Lunch, Dinner
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

-- 14. MEAL_FOODS
--     Foods consumed in specific meals with quantities
CREATE TABLE meal_foods (
    meal_food_id SERIAL PRIMARY KEY,
    meal_id INT NOT NULL,
    user_food_id INT NOT NULL,
    quantity_consumed NUMERIC(6,2) NOT NULL,
    FOREIGN KEY (meal_id) REFERENCES meals(meal_id),
    FOREIGN KEY (user_food_id) REFERENCES user_foods(user_food_id)
);

-- 15. NUTRITION_RECOMMENDATION
--     Predefined nutrition recommendations catalog
CREATE TABLE nutrition_recommendation (
    recommendation_id SERIAL PRIMARY KEY,
    description TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 16. NUTRITION_RECOMMENDATION_FEEDBACK
--     User feedback on nutrition recommendations (positive/negative)
CREATE TABLE nutrition_recommendation_feedback (
    user_id INT NOT NULL,
    recommendation_id INT NOT NULL,
    is_positive SMALLINT NOT NULL, -- values: 0 or 1
    noted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    comment TEXT,
    PRIMARY KEY (user_id, recommendation_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (recommendation_id) REFERENCES nutrition_recommendation(recommendation_id)
);

-- 17. WORKOUT_RECOMMENDATION
--     Predefined workout recommendations catalog
CREATE TABLE workout_recommendation (
    recommendation_id SERIAL PRIMARY KEY,
    description TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 18. WORKOUT_RECOMMENDATION_FEEDBACK
--     User feedback on workout recommendations (positive/negative)
CREATE TABLE workout_recommendation_feedback (
    user_id INT NOT NULL,
    recommendation_id INT NOT NULL,
    is_positive SMALLINT NOT NULL, -- values: 0 or 1
    noted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    comment TEXT,
    PRIMARY KEY (user_id, recommendation_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (recommendation_id) REFERENCES workout_recommendation(recommendation_id)
);

-- 19. NUTRITION_RECOMMENDATION_BLACKLIST
--     User blacklist for nutrition recommendations to exclude
CREATE TABLE nutrition_recommendation_blacklist (
    user_id INT NOT NULL,
    recommendation_id INT NOT NULL,
    noted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, recommendation_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (recommendation_id) REFERENCES nutrition_recommendation(recommendation_id)
);

-- 20. WORKOUT_RECOMMENDATION_BLACKLIST
--     User blacklist for workout recommendations to exclude
CREATE TABLE workout_recommendation_blacklist (
    user_id INT NOT NULL,
    recommendation_id INT NOT NULL,
    noted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id, recommendation_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (recommendation_id) REFERENCES workout_recommendation(recommendation_id)
);

-- 21. Activity
--     Activity included in the datatbase
CREATE TABLE activity_default(
    activity_id INT NOT NULL,
    name VARCHAR(255) NOT NULL,
    icon VARCHAR(20)
);

-- 21. Activity
--     sensor_status
CREATE TABLE IF NOT EXISTS sensor_status (
  sensor_id  INT PRIMARY KEY REFERENCES sensor_to_user(sensor_id) ON DELETE CASCADE,
  active     BOOLEAN NOT NULL DEFAULT TRUE,
  updated_at TIMESTAMPTZ DEFAULT now()
);

-- Funzione trigger (PL/pgSQL)
CREATE OR REPLACE FUNCTION sensor_status_autocreate()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO sensor_status (sensor_id, active, updated_at)
  VALUES ((NEW).sensor_id, TRUE, now())
  ON CONFLICT (sensor_id) DO NOTHING;
  RETURN NULL;  -- AFTER trigger: il ritorno è ignorato per convenzione
END;
$$ LANGUAGE PLpgSQL;

-- Trigger: dopo ogni INSERT su sensor_to_user
DROP TRIGGER IF EXISTS trg_sensor_status_autocreate ON sensor_to_user;

CREATE TRIGGER trg_sensor_status_autocreate
AFTER INSERT ON sensor_to_user
FOR EACH ROW
EXECUTE FUNCTION sensor_status_autocreate();

